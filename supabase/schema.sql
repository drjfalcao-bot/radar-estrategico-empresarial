create or replace function public.set_user_access(target_user uuid, new_status text)
returns public.profiles
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_profile public.profiles;
begin
  if not public.is_admin() then
    raise exception 'Apenas administradores podem alterar acessos.';
  end if;

  if new_status not in ('approved','rejected','suspended','pending') then
    raise exception 'Status inválido.';
  end if;

  update public.profiles
  set approval_status = new_status,
      approved_at = case when new_status = 'approved' then now() else approved_at end,
      approved_by = case when new_status = 'approved' then auth.uid() else approved_by end
  where id = target_user
  returning * into updated_profile;

  insert into public.access_audit(actor_user_id,target_user_id,action,details)
  values(auth.uid(),target_user,'access_' || new_status,jsonb_build_object('status',new_status));

  return updated_profile;
end;
$$;

create or replace function public.set_user_role(target_user uuid, new_role text)
returns public.profiles
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_profile public.profiles;
begin
  if not public.is_admin() then
    raise exception 'Apenas administradores podem alterar perfis.';
  end if;

  if new_role not in ('user','admin') then
    raise exception 'Perfil inválido.';
  end if;

  update public.profiles
  set role = new_role
  where id = target_user
  returning * into updated_profile;

  insert into public.access_audit(actor_user_id,target_user_id,action,details)
  values(auth.uid(),target_user,'role_changed',jsonb_build_object('role',new_role));

  return updated_profile;
end;
$$;

create or replace function public.assign_lead(target_lead text, target_owner uuid)
returns public.leads
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_lead public.leads;
begin
  if not public.is_admin() then
    raise exception 'Apenas administradores podem reatribuir leads.';
  end if;

  if not exists (
    select 1 from public.profiles
    where id = target_owner and approval_status = 'approved'
  ) then
    raise exception 'O responsável precisa estar aprovado.';
  end if;

  update public.leads
  set owner_user_id = target_owner
  where id = target_lead
  returning * into updated_lead;

  insert into public.access_audit(actor_user_id,target_user_id,lead_id,action,details)
  values(auth.uid(),target_owner,target_lead,'lead_reassigned',jsonb_build_object('owner_user_id',target_owner));

  return updated_lead;
end;
$$;

grant execute on function public.set_user_access(uuid,text) to authenticated;
grant execute on function public.set_user_role(uuid,text) to authenticated;
grant execute on function public.assign_lead(text,uuid) to authenticated;

